"""協和性計算のための物理的および音響的定数。

このモジュールは、Setharesの音響的ラフネスモデルおよび関連する
心理音響計算で使用される定数を定義します。

References:
    - Sethares, W. A. (1993). "Local consonance and the relationship between
      timbre and scale." Journal of the Acoustical Society of America, 94(3).
    - Plomp, R., & Levelt, W. J. M. (1965). "Tonal consonance and critical
      bandwidth." Journal of the Acoustical Society of America, 38.
"""

# ============================================================================
# チューニングシステム定数
# ============================================================================

#: A4の標準基準周波数(国際標準)
DEFAULT_BASE_FREQUENCY: float = 440.0  # Hz

#: ピッチ計算のための基準周波数
REFERENCE_FREQUENCY: float = 440.0  # Hz (A4)

# ============================================================================
# 倍音系列定数
# ============================================================================

#: 考慮するデフォルトの倍音の数(要件ごと:1-10)
DEFAULT_NUM_HARMONICS: int = 10

# ============================================================================
# Setharesラフネスモデル定数
# ============================================================================

#: 不協和曲線の形状パラメータb1
#: 不協和曲線の上昇勾配を制御します
ROUGHNESS_B1: float = 3.5

#: 不協和曲線の形状パラメータb2
#: 不協和曲線の下降勾配を制御します
ROUGHNESS_B2: float = 5.75

# ============================================================================
# 臨界帯域幅定数(単純化されたPlomp-Leveltモデル)
# ============================================================================

#: 臨界帯域幅係数:CB(f) = CB_COEFFICIENT * f + CB_CONSTANT
#: 計算効率のためのバークスケールの単純化された近似
CB_COEFFICIENT: float = 0.24

#: 臨界帯域幅の定数項(Hz)
CB_CONSTANT: float = 25.0

# ============================================================================
# MVPでサポートされているシステム
# ============================================================================

#: MVPでサポートされているEDOシステム
SUPPORTED_EDO_SYSTEMS: tuple[int, ...] = (12, 19)

# ============================================================================
# 比較のための参照和音
# ============================================================================

#: 比較のための参照和音:12-EDOのCメジャートライアド[C、E、G]
#: Cから始まる12-EDOのステップインデックス
REFERENCE_CHORD_12EDO_MAJOR: tuple[int, ...] = (0, 4, 7)
